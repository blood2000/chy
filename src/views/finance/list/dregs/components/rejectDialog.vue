<template>
  <!-- 驳回理由对话框 -->
  <el-dialog :title="title" :visible="visible" width="800px" append-to-body :close-on-click-modal="false" @close="cancel">
    <el-form ref="form" :model="form" :rules="rules" label-width="130px">
      <el-form-item label="驳回原因" prop="rebutRemark">
        <el-input v-model="form.rebutRemark" type="textarea" maxlength="50" show-word-limit :autosize="{ minRows: 2, maxRows: 4}" placeholder="请输入驳回理由" style="width:90%;" clearable />
      </el-form-item>
    </el-form>
    <div slot="footer" class="dialog-footer">
      <el-button type="primary" @click="submitForm">立即提交</el-button>
      <el-button @click="cancel">取消</el-button>
    </div>
  </el-dialog>
</template>

<script>

import { refuseBilling } from '@/api/finance/list';

export default {
  name: 'RejectDialog',
  props: {
    title: {
      type: String,
      default: ''
    },
    open: Boolean
  },
  data() {
    return {
      // 表单参数
      form: {
      },
      // 表单校验
      rules: {
        rebutRemark: [
          { required: true, message: '驳回原因不能为空', trigger: 'blur' }
        ]
      }
    };
  },
  computed: {
    visible: {
      get() {
        return this.open;
      },
      set(v) {
        this.$emit('update:open', v);
      }
    }
  },
  created() {
  },
  methods: {
    /** 提交按钮 */
    submitForm() {
      this.$refs['form'].validate(valid => {
        if (valid) {
          const que = {
            batchCodes: [this.form.waybillCode], //	批次列表不能为空		false
            createCode: this.form.createCode,
            remark: this.form.rebutRemark
          };
          refuseBilling(que).then(response => {
            this.msgSuccess('驳回运单成功');
            this.close();
            this.$emit('refresh');
          });
        }
      });
    },
    /** 取消按钮 */
    cancel() {
      this.close();
      this.reset();
    },
    // 关闭弹窗
    close() {
      this.$emit('update:open', false);
    },
    // 表单重置
    reset() {
      this.form = {
        waybillCode: null,
        rebutRemark: null,
        createCode: null
      };
      this.resetForm('form');
    },
    // 表单赋值
    setForm(data, createCode) {
      this.form.waybillCode = data.batchNo;
      this.form.createCode = createCode;
    }
  }
};
</script>

