<template>
  <!-- 添加或修改调度者对话框 -->
  <el-dialog :title="title" :visible="visible" width="800px" append-to-body :close-on-click-modal="disable" @close="cancel">
    <el-form ref="form" :model="form" :rules="rules" :disabled="disable" label-width="140px">
      <el-form-item label="调度者名称" prop="name">
        <el-input v-model="form.name" placeholder="请输入调度者名称" class="width90" clearable />
      </el-form-item>
      <el-form-item label="状态">
        <el-radio-group v-model="form.status">
          <el-radio
            v-for="dict in statusOptions"
            :key="dict.dictValue"
            :label="parseInt(dict.dictValue)"
          >{{ dict.dictLabel }}</el-radio>
        </el-radio-group>
      </el-form-item>
      <el-form-item label="姓名" prop="teamLeaderName">
        <el-input v-model="form.teamLeaderName" placeholder="支持自动识别" class="width90" clearable />
      </el-form-item>
      <el-form-item label="手机号/账号" prop="telphone">
        <el-input v-model="form.telphone" placeholder="请输入手机号/账号" class="width90" clearable />
      </el-form-item>
      <el-form-item label="密码" prop="password">
        <el-input v-model="form.password" type="password" placeholder="请输入密码" class="width60 mr3" clearable />
        <span class="g-color-blue">(初始密码为{{ initialPassword }})</span>
      </el-form-item>
      <el-form-item label="身份证号" prop="identificationNumber">
        <el-input v-model="form.identificationNumber" placeholder="支持自动识别" class="width90" clearable />
      </el-form-item>
      <el-form-item prop="identificationEndTime">
        <label slot="label"><span style="color: #ff4949">* </span>身份证有效期</label>
        <el-date-picker
          v-model="form.identificationBeginTime"
          clearable
          class="width28"
          type="date"
          value-format="yyyy-MM-dd"
          placeholder="支持自动识别"
        />
        至
        <el-date-picker
          v-model="form.identificationEndTime"
          clearable
          class="width28 mr3"
          type="date"
          value-format="yyyy-MM-dd"
          placeholder="支持自动识别"
          :disabled="!!form.identificationEffective"
        />
        <el-checkbox v-model="form.identificationEffective" @change="handleCheckChange">长期有效</el-checkbox>
      </el-form-item>
      <el-form-item label="是否清分" prop="isDistribution">
        <el-select
          v-model="form.isDistribution"
          clearable
          filterable
          class="width90"
        >
          <el-option
            v-for="dict in isOptions"
            :key="dict.dictValue"
            :label="dict.dictLabel"
            :value="dict.dictValue"
          />
        </el-select>
      </el-form-item>
      <el-form-item label="清分百分比(%)" prop="distributionPercent">
        <el-input-number v-model="form.distributionPercent" controls-position="right" :precision="2" placeholder="请输入清分百分比" :step="1" :min="0" :max="100" class="width90" clearable />
      </el-form-item>
      <el-form-item>
        <el-row>
          <el-col :span="7">
            <p class="upload-image-label">身份证正面照</p>
            <uploadImage v-model="form.identificationImage" :disabled="disable" image-type="id-card" side="front" @fillForm="fillForm" />
          </el-col>
          <el-col :span="7">
            <p class="upload-image-label">身份证反面照</p>
            <uploadImage v-model="form.identificationBackImage" :disabled="disable" image-type="id-card" side="back" @fillForm="fillForm" />
          </el-col>
          <el-col :span="7">
            <p class="upload-image-label">营业执照</p>
            <uploadImage v-model="form.businessLicenseImg" :disabled="disable" image-type="business-license" @fillForm="fillForm" />
          </el-col>
          <el-col :span="7">
            <p class="upload-image-label">道路运输经营许可证照</p>
            <uploadImage v-model="form.transportPermitImage" :disabled="disable" />
          </el-col>
        </el-row>
      </el-form-item>
    </el-form>
    <div v-if="title === '新增' || title === '编辑'" slot="footer" class="dialog-footer">
      <el-button type="primary" @click="submitForm">确 定</el-button>
      <el-button @click="cancel">取 消</el-button>
    </div>
    <div v-if="title === '审核'" slot="footer" class="dialog-footer">
      <el-button type="primary" @click="reviewForm(3)">审核通过</el-button>
      <el-button type="danger" @click="reviewForm(2)">审核不通过</el-button>
    </div>
  </el-dialog>
</template>

<script>
import { addInfo, updateInfo, authRead, examine } from '@/api/assets/team';
import UploadImage from '@/components/UploadImage/index';
import { praseBooleanToNum, praseNumToBoolean, compareTime } from '@/utils/ddc';

export default {
  components: {
    UploadImage
  },
  props: {
    title: {
      type: String,
      default: ''
    },
    open: Boolean,
    disable: Boolean
  },
  data() {
    return {
      // 初始密码
      initialPassword: 'abcd1234@',
      // 状态字典
      statusOptions: [
        { dictLabel: '启用', dictValue: 0 },
        { dictLabel: '禁用', dictValue: 1 }
      ],
      isOptions: [
        { dictLabel: '否', dictValue: 0 },
        { dictLabel: '是', dictValue: 1 }
      ],
      // 表单参数
      form: {},
      // 表单校验
      rules: {
        telphone: [
          { required: true, message: '手机号/账号不能为空', trigger: 'blur' },
          { validator: this.formValidate.telphone, trigger: 'blur' }
        ],
        name: [
          { required: true, message: '调度者名称不能为空', trigger: 'blur' }
        ],
        teamLeader: [
          { required: true, message: '管理者名称不能为空', trigger: 'blur' }
        ],
        identificationNumber: [
          { required: true, message: '身份证号不能为空', trigger: 'blur' },
          { validator: this.formValidate.idCard, trigger: 'blur' }
        ],
        identificationEndTime: [
          { validator: this.formValidate.isExpired, trigger: 'change' },
          { validator: (rules, value, callback) => {
            const { identificationBeginTime, identificationEffective } = this.form;
            if (!identificationBeginTime) {
              return callback(new Error('身份证有效期起始时间不能为空'));
            } else if (!identificationEffective && !value) {
              return callback(new Error('身份证有效期截止时间不能为空'));
            } else if (!compareTime(identificationBeginTime, value)) {
              return callback(new Error('身份证有效期截止时间不能小于起始时间'));
            }
            return callback();
          },
          trigger: ['change', 'blur']
          }
        ],
        password: [
          { validator: this.formValidate.passWord, trigger: 'blur' }
        ]
      }
    };
  },
  computed: {
    visible: {
      get() {
        return this.open;
      },
      set(v) {
        this.$emit('update:open', v);
      }
    }
  },
  methods: {
    /** 提交按钮 */
    submitForm() {
      this.$refs['form'].validate(valid => {
        if (valid) {
          this.form.identificationEffective = praseBooleanToNum(this.form.identificationEffective);
          if (this.form.id) {
            updateInfo(this.form).then(response => {
              this.msgSuccess('修改成功');
              this.close();
              this.$emit('refresh');
            });
          } else {
            addInfo(this.form).then(response => {
              this.msgSuccess('新增成功');
              this.close();
              this.$emit('refresh');
            });
          }
        }
      });
    },
    /** 取消按钮 */
    cancel() {
      this.close();
      this.reset();
    },
    // 关闭弹窗
    close() {
      this.$emit('update:open', false);
    },
    // 表单重置
    reset() {
      this.form = {
        id: null,
        code: null,
        branchCode: null,
        name: null,
        teamLeader: null,
        isDel: null,
        status: 0,
        createCode: null,
        createTime: null,
        updateCode: null,
        updateTime: null,
        businessLicenseImg: null,
        transportPermitImage: null,
        identificationImage: null,
        identificationBackImage: null,
        telphone: null,
        password: null,
        identificationNumber: null,
        identificationBeginTime: null,
        identificationEndTime: null,
        identificationEffective: null,
        isDistribution: null,
        distributionPercent: null
      };
      this.resetForm('form');
    },
    // 表单赋值
    setForm(data) {
      this.form = data;
      this.form.identificationEffective = praseNumToBoolean(this.form.identificationEffective);
      this.handleCheckChange(this.form.identificationEffective);
    },
    // 已读
    authRead() {
      authRead({
        authStatus: 1,
        code: this.form.code
      }).then(response => {
        this.$emit('refresh');
      });
    },
    /** 审核通过/未通过按钮 */
    reviewForm(key) {
      examine({
        authStatus: key,
        code: this.form.code
      }).then(response => {
        this.msgSuccess('操作成功');
        this.close();
        this.$emit('refresh');
      });
    },
    // 身份证是否长期有效选中事件
    handleCheckChange(val) {
      if (val) {
        this.form.identificationEndTime = null;
      }
    },
    // 图片识别后回填
    fillForm(type, data) {
      switch (type) {
        case 'id-card':
          if (data.name) this.form.teamLeaderName = data.name;
          if (data.number) this.form.identificationNumber = data.number;
          if (data.valid_from) this.form.identificationBeginTime = data.valid_from;
          if (data.valid_to && !this.form.identificationEffective) this.form.identificationEndTime = data.valid_to;
          break;
        case 'business-license':
          break;
        default:
          break;
      }
    }
  }
};
</script>

